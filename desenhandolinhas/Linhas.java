package desenhandolinhas;
/**
 * Linhas
 */
public class Linhas {
    public static void main(String[] args) throws Exception {
        // Linha horizontal
        for (int j = 0; j < 10; j++) {
            System.out.print('8');
        }
        System.out.println();

        // Linha vertical
        for (int i = 0; i < 10; i++) {
            System.out.println('8');
        }
    }
}