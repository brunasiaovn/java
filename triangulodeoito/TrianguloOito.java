package triangulodeoito;

public class TrianguloOito {
    public static void main(String[] args) throws Exception {
        int m = 10;

        for (int i = 0; i < m; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print('8');
            }

            System.out.println();
        }
    }
}
